package com.example.chat_application;

public class ReadAndWriteUserData {
    private String name;
    private String email;
    private String imageURL;
    private String status;
    private String doB;
    private String gender;
    private String phone;

    public ReadAndWriteUserData( String name, String email, String imageURL, String status, String doB, String gender, String phone) {
        this.name = name;
        this.email = email;
        this.imageURL = imageURL;
        this.status = status;
        this.doB = doB;
        this.gender = gender;
        this.phone = phone;
    }

}
